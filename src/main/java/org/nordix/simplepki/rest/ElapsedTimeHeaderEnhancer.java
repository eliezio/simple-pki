package org.nordix.simplepki.rest;

import org.nordix.simplepki.clock.Timer;
import lombok.val;
import org.springframework.core.MethodParameter;
import org.springframework.http.MediaType;
import org.springframework.http.converter.HttpMessageConverter;
import org.springframework.http.server.ServerHttpRequest;
import org.springframework.http.server.ServerHttpResponse;
import org.springframework.http.server.ServletServerHttpRequest;
import org.springframework.http.server.ServletServerHttpResponse;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.servlet.mvc.method.annotation.ResponseBodyAdvice;

/**
 * Assumes that the Timer.start() was called at the beginning of the request handling.
 * A good location is from inside a HandlerInterceptorAdapter::preHandle() invocation.
 */
@ControllerAdvice
public class ElapsedTimeHeaderEnhancer implements ResponseBodyAdvice<Object> {

    @Override
    public boolean supports(MethodParameter returnType, Class<? extends HttpMessageConverter<?>> converterType) {
        return true;
    }

    @Override
    public Object beforeBodyWrite(Object body, MethodParameter returnType, MediaType selectedContentType,
                                  Class<? extends HttpMessageConverter<?>> selectedConverterType,
                                  ServerHttpRequest serverRequest, ServerHttpResponse serverResponse) {
        val request = ((ServletServerHttpRequest) serverRequest).getServletRequest();
        val response = ((ServletServerHttpResponse) serverResponse).getServletResponse();
        Timer timer = HttpRequestTimer.load(request);
        if (timer != null) {
            response.setIntHeader("Elapsed-Time", (int) timer.elapsed());
        }
        return body;
    }
}
